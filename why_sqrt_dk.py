import torch                    # библиотека для работы с тензорами и автодифференцированием
import torch.nn.functional as F  # функции без весов: softmax, relu и т.д.

torch.manual_seed(42)
# Фиксируем генератор случайных чисел — чтобы каждый запуск давал одинаковый результат
# Без этого randn каждый раз даёт разные числа → трудно сравнивать

d_k = 64
# d_k — размерность вектора Query и Key (сколько чисел в одном векторе токена)
# В оригинальной статье d_k = 64 при 8 головах и d_model = 512

q = torch.randn(d_k)
# Создаём один вектор запроса из 64 случайных чисел из N(0,1)
# N(0,1) означает: среднее=0, стандартное отклонение=1

k = torch.randn(d_k)
# Создаём один вектор ключа — тоже из 64 чисел

dot = (q * k).sum()
# q * k — поэлементное умножение двух векторов (64 пары чисел перемножаются)
# .sum() — складываем все 64 произведения в одно число
# Это и есть dot product (скалярное произведение): q · k = Σ q_i * k_i
# Показывает "насколько похожи" два вектора

print(f"d_k = {d_k}")
print(f"Dot product: {dot:.2f}")
# Смотрим насколько большим получился dot product
# При d_k=64 он будет примерно в районе ±8 (√64=8)

keys = torch.randn(6, d_k)
# Создаём 6 ключей (имитируем 6 токенов в последовательности)
# форма: (6, 64) — 6 векторов по 64 числа каждый

scores_raw = keys @ q
# @ — оператор матричного умножения
# keys: (6, 64), q: (64,) → результат: (6,)
# Каждый элемент = dot product q с одним из 6 ключей
# Показывает "насколько каждый токен релевантен запросу q"

print(f"\nScores БЕЗ деления: {scores_raw.round(decimals=1).tolist()}")
# Смотрим сырые оценки — они большие потому что d_k=64 большой

print(f"Дисперсия scores:   {scores_raw.var():.1f}  (ожидаем ≈ {d_k})")
# .var() — дисперсия (разброс чисел)
# Математически: если q и k из N(0,1), то Var(q·k) = d_k
# Значит при d_k=64 дисперсия scores ≈ 64 → числа в диапазоне примерно ±8

scores_scaled = scores_raw / d_k**0.5
# d_k**0.5 = √64 = 8
# Делим все scores на 8 → дисперсия уменьшается с 64 до 64/64=1
# Теперь числа в разумном диапазоне ≈ ±1

print(f"\nScores ПОСЛЕ деления на √{d_k}={d_k**0.5:.0f}: {scores_scaled.round(decimals=1).tolist()}")
print(f"Дисперсия scores:   {scores_scaled.var():.1f}  (ожидаем ≈ 1)")

print("\n--- Softmax ---")
print(f"Без деления:  {F.softmax(scores_raw, dim=0).round(decimals=3).tolist()}")
# softmax превращает 6 чисел в 6 весов (сумма=1)
# При больших числах — один вес близок к 1, остальные к 0 (насыщение)
# Модель "смотрит" только на один токен и игнорирует все остальные

print(f"С делением:   {F.softmax(scores_scaled, dim=0).round(decimals=3).tolist()}")
# При нормализованных числах — веса распределены равномернее
# Модель учится смотреть на несколько токенов одновременно

print("\n--- Что происходит с градиентом ---")
for scale in [1, 4, 10]:
    # Проверяем что происходит при разных масштабах входа в softmax

    x = torch.tensor([2.0, 1.0, 0.5, -1.0]) * scale
    # Умножаем базовые scores на scale (1, 4, 10)
    # scale=1: нормальные числа; scale=10: очень большие числа

    x.requires_grad_(True)
    # Говорим PyTorch: "отслеживай операции с x, нам нужен градиент"
    # Градиент показывает как изменение x влияет на выход — нужен для обучения

    y = F.softmax(x, dim=0)
    # Применяем softmax к нашим тестовым scores

    y.sum().backward()
    # .backward() — вычисляем градиенты (backpropagation)
    # Цепочка: loss → y → x, нас интересует d(loss)/d(x)

    print(f"scale={scale:2d} | softmax={y.detach().round(decimals=3).tolist()} | grad max={x.grad.abs().max():.4f}")
    # x.grad — градиент (как сильно каждый x влияет на выход)
    # При насыщении (scale=10) softmax выдаёт [1,0,0,0] → градиент ≈ 0
    # Градиент ≈ 0 → обучение останавливается (нечего исправлять)
    # Это называется "проблема затухающих градиентов"
